calculo_prop<-  function(x, y){
      modelo <- lm(y ~ x)
      list(
        media_x = mean(x),
        var_x = var(x),
        media_y = mean(y),
        var_y = var(y),
        correlacion = cor(x,y),
        regresion = coef(modelo),
        R2 = summary(modelo)$r.squared
      )
}
